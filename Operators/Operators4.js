// Not Equal

let num5 = 12;
let num6 = 13;
console.log(num5 != num6);

let num7 = 15;
let num8 = 15;
console.log(num7 != num8);

// Strictly Equal

let same1 = 45;
let same2 = 45;
console.log(same1 === same2);

let same3 = 45;
let same4 = "45";
console.log(same3 === same4);

// Strictly Not Equal

let same5 = 15;
let same6 = 15;
console.log(same5 !== same6);

let same7 = 18;
let same8 = "18";
console.log(same7 !== same8);

// Lesser Then

